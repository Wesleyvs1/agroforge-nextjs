-- 3. Criação do Bucket de Imagens
-- Nota: Isso cria o bucket se a extensão storage_admin estiver habilitada, 
-- caso contrário você pode criar manualmente com o nome "media" marcando como 'Public'.
insert into storage.buckets (id, name, public)
values ('media', 'media', true)
on conflict (id) do nothing;

create policy "Public Access"
  on storage.objects for select
  using ( bucket_id = 'media' );

create policy "Public inserts"
  on storage.objects for insert
  with check ( bucket_id = 'media' );

create policy "Public updates"
  on storage.objects for update
  using ( bucket_id = 'media' );

create policy "Public deletes"
  on storage.objects for delete
  using ( bucket_id = 'media' );
