CREATE POLICY "Enable insert for everyone" ON public.produtos FOR INSERT WITH CHECK (true);
CREATE POLICY "Enable update for everyone" ON public.produtos FOR UPDATE USING (true);
CREATE POLICY "Enable delete for everyone" ON public.produtos FOR DELETE USING (true);

